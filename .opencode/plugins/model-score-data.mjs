function normalizeModelID(modelID) {
  return String(modelID ?? "")
    .trim()
    .toLowerCase()
    .replace(/\s+/g, "-")
    .replace(/_/g, "-");
}

function bareModelID(modelID) {
  const normalized = String(modelID ?? "");
  return normalized.includes("/") ? normalized.split("/").at(-1) : normalized;
}

function lookupKeys(modelID) {
  const full = normalizeModelID(modelID);
  const bare = normalizeModelID(bareModelID(full));
  return new Set([full, bare].filter(Boolean));
}

function findByAlias(rows, modelID) {
  const keys = lookupKeys(modelID);
  return rows.find((row) => row.aliases.some((alias) => keys.has(normalizeModelID(alias))));
}

function roundScore(score) {
  return Math.round(score);
}

function formatCurrency(value) {
  if (typeof value !== "number") return "n/a";
  return `$${value.toFixed(2)}`;
}

function formatDecimal(value, digits = 2) {
  if (typeof value !== "number") return "n/a";
  return value.toFixed(digits);
}

function formatInteger(value) {
  if (typeof value !== "number") return "n/a";
  return new Intl.NumberFormat("en-US", { maximumFractionDigits: 0 }).format(value);
}

function resolveModelBenchmark(modelID) {
  const benchmark = findByAlias(MODEL_BENCHMARKS, modelID);
  if (!benchmark) {
    return {
      score: undefined,
      displayScore: "n/a",
      rawScore: undefined,
      benchmark: "Terminal-Bench 2.1",
      source: "https://www.tbench.ai/leaderboard/terminal-bench/2.1",
      harnesses: [],
    };
  }

  return {
    ...benchmark,
    displayScore: String(roundScore(benchmark.score)),
  };
}

function resolveModelCost(modelID) {
  const cost = findByAlias(MODEL_COSTS, modelID);
  if (!cost) {
    return {
      input: undefined,
      output: undefined,
      cached: undefined,
      tokenEfficiency: undefined,
      tokenEfficiencySource: undefined,
      tokenEfficiencyBenchmark: undefined,
      tokenEfficiencyHarness: undefined,
      tokenEfficiencyModel: undefined,
      tokenEfficiencyFormula: undefined,
      benchmarkScore: undefined,
      inputTokens: undefined,
      answerTokens: undefined,
      reasoningTokens: undefined,
      outputTokens: undefined,
      evalCost: undefined,
      costPerSession: undefined,
      tokensPerSession: undefined,
      tokensPerDollar: undefined,
      source: "https://opencode.ai/data/",
    };
  }

  return cost;
}

export function resolveModelStats(modelID) {
  const normalizedModelID = normalizeModelID(modelID);
  const benchmark = resolveModelBenchmark(normalizedModelID);
  const cost = resolveModelCost(normalizedModelID);

  return {
    modelID: normalizedModelID || "unknown",
    score: benchmark.score === undefined ? undefined : roundScore(benchmark.score),
    displayScore: benchmark.displayScore,
    benchmark,
    cost,
  };
}

export function resolveModelScore(modelID) {
  const stats = resolveModelStats(modelID);
  return {
    modelID: stats.modelID,
    score: stats.score ?? 0,
    displayScore: stats.displayScore,
    rawScore: stats.benchmark.score,
    method: stats.score === undefined ? "terminal-bench-missing" : `${stats.benchmark.benchmark.toLowerCase().replace(/\s+/g, "-")}-average`,
    confidence: stats.score === undefined ? "none" : "benchmark",
    benchmark: stats.benchmark.benchmark,
    source: stats.benchmark.source,
    evidence: stats.benchmark.harnesses.map((harness) => ({
      kind: "terminal-bench-harness",
      weight: 1 / stats.benchmark.harnesses.length,
      score: harness.score,
      benchmark: stats.benchmark.benchmark,
      match: `${harness.agent} + ${harness.model}`,
      source: harness.source,
    })),
    cost: stats.cost,
  };
}

function asModelStats(resultOrStats) {
  if (resultOrStats?.benchmark && typeof resultOrStats.benchmark === "object") return resultOrStats;
  return resolveModelStats(resultOrStats?.modelID);
}

export function formatModelScoreSummary(resultOrStats) {
  const stats = asModelStats(resultOrStats);
  const cost = stats.cost ?? {};
  return `model score: ${stats.displayScore} | token cost (in / out): ${formatCurrency(cost.input)} / ${formatCurrency(cost.output)} per 1M | token efficiency: ${formatDecimal(cost.tokenEfficiency)}`;
}

export function formatModelScoreBreakdown(resultOrStats) {
  const stats = asModelStats(resultOrStats);
  const benchmark = stats.benchmark;
  const cost = stats.cost ?? {};
  const lines = [
    `model: ${stats.modelID}`,
    `model score: ${stats.displayScore}`,
    `benchmark: ${benchmark.benchmark}`,
    `score method: Terminal-Bench 2.1 is preferred, Terminal-Bench 2.0 is used as fallback, then Artificial Analysis Terminal-Bench v2.1 is used when official rows are missing`,
    `source: ${benchmark.source}`,
    "harnesses:",
  ];

  if (benchmark.harnesses.length === 0) {
    lines.push("- none found");
  } else {
    for (const harness of benchmark.harnesses) {
      const modelLabel = harness.modelStack ? `${harness.model} via ${harness.modelStack}` : harness.model;
      const details = [modelLabel, harness.date, `${harness.agentName}@${harness.agentVersion}`].filter(Boolean).join(", ");
      lines.push(`- ${harness.agent}: ${formatDecimal(harness.score, 1)} (${details})`);
    }
  }

  lines.push("token cost:");
  lines.push(`- input: ${formatCurrency(cost.input)} per 1M tokens`);
  lines.push(`- output: ${formatCurrency(cost.output)} per 1M tokens`);
  if (typeof cost.cached === "number") lines.push(`- cached input: ${formatCurrency(cost.cached)} per 1M tokens`);
  lines.push("token efficiency:");
  lines.push(`- score: ${formatDecimal(cost.tokenEfficiency)}`);
  lines.push(`- source: ${cost.tokenEfficiencySource ?? "n/a"}`);
  lines.push(`- benchmark: ${cost.tokenEfficiencyBenchmark ?? "n/a"}`);
  if (cost.tokenEfficiencyHarness) lines.push(`- harness: ${cost.tokenEfficiencyHarness}`);
  if (cost.tokenEfficiencyModel) lines.push(`- measured model: ${cost.tokenEfficiencyModel}`);
  lines.push(`- formula: ${cost.tokenEfficiencyFormula ?? "n/a"}`);
  if (typeof cost.benchmarkScore === "number") lines.push(`- benchmark score: ${formatDecimal(cost.benchmarkScore, 1)}`);
  if (typeof cost.outputTokens === "number") lines.push(`- output tokens: ${formatInteger(cost.outputTokens)}`);
  if (typeof cost.answerTokens === "number") lines.push(`- answer tokens: ${formatInteger(cost.answerTokens)}`);
  if (typeof cost.reasoningTokens === "number") lines.push(`- reasoning tokens: ${formatInteger(cost.reasoningTokens)}`);
  if (typeof cost.evalCost === "number") lines.push(`- benchmark eval cost: ${formatCurrency(cost.evalCost)}`);
  if (typeof cost.costPerSession === "number") lines.push(`- average cost per session: ${formatCurrency(cost.costPerSession)}`);
  if (typeof cost.tokensPerSession === "number") lines.push(`- average tokens per session: ${formatInteger(cost.tokensPerSession)}`);
  if (typeof cost.tokensPerDollar === "number") lines.push(`- tokens per dollar: ${formatInteger(cost.tokensPerDollar)}`);
  lines.push(`cost source: ${cost.source ?? "https://opencode.ai/data/"}`);

  return lines.join("\n");
}

export function modelRefToID(modelRef) {
  if (!modelRef || typeof modelRef !== "object") return undefined;

  const providerID = typeof modelRef.providerID === "string" ? modelRef.providerID.trim() : "";
  const modelID = typeof modelRef.modelID === "string" ? modelRef.modelID.trim() : "";
  const id = typeof modelRef.id === "string" ? modelRef.id.trim() : "";

  if (providerID && modelID) return `${providerID}/${modelID}`;
  if (id.includes("/")) return id;
  if (providerID && id) return `${providerID}/${id}`;
  if (modelID) return modelID;
  if (id) return id;
  return undefined;
}

export function readSelectedModelIDFromState(statePath) {
  if (typeof statePath !== "string" || !statePath.trim()) return undefined;

  try {
    const raw = readFileSync(path.join(statePath, "model.json"), "utf8");
    const parsed = JSON.parse(raw);
    const recent = Array.isArray(parsed?.recent) ? parsed.recent : [];
    return modelRefToID(recent[0]);
  } catch {
    return undefined;
  }
}

export function resolveCurrentModelID(api) {
  const selected = readSelectedModelIDFromState(api?.state?.path?.state);
  if (selected) return selected;

  const configured = modelRefToID(api?.state?.config?.model);
  if (configured) return configured;

  return "unknown";
}
